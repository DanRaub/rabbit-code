    // Inject styles once
    if (!document.getElementById('jabsco-trainer-styles')) {
      var styleEl = document.createElement('style');
      styleEl.id = 'jabsco-trainer-styles';
      styleEl.textContent = CSS;
      document.head.appendChild(styleEl);
    }

    // Find mount: data-mount attribute on this script, or #jabsco-trainer, or new div
    var mount = null;
    var scripts = document.getElementsByTagName('script');
    for (var i = scripts.length - 1; i >= 0; i--) {
      var src = scripts[i].getAttribute('src') || '';
      if (src.indexOf('jabsco-trainer') !== -1 && scripts[i].hasAttribute('data-mount')) {
        mount = document.querySelector(scripts[i].getAttribute('data-mount'));
        break;
      }
    }
    if (!mount) mount = document.getElementById('jabsco-trainer');
    if (!mount) {
      mount = document.createElement('div');
      mount.id = 'jabsco-trainer';
      document.body.appendChild(mount);
    }

    // Inject markup
    mount.innerHTML = HTML;

    // Run the trainer's own JS, which is already wrapped in an IIFE and handles
    // its own DOM-ready bootstrapping.
    runTrainer();
