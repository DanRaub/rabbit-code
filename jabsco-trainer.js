// ===== Solver =====
function solve() {
  const r = { pumps: { port:{}, stbd:{} }, alerts: [] };
  const openByside = { port:[], stbd:[] };
  for (const k in ZONES) if (state.zones[k]) openByside[ZONES[k].side].push(k);

  const eitherPumpOn = state.pumps.port || state.pumps.stbd;
  const totalOpenZones = openByside.port.length + openByside.stbd.length;
  const fireOpen = (state.threeWay.port === 'fire') || (state.threeWay.stbd === 'fire');
  const crossoverViolation = state.crossover && eitherPumpOn && (
    (state.pumps.port && state.pumps.stbd) ||
    (totalOpenZones > 1) ||
    fireOpen
  );

  if (state.crossover && state.pumps.port && state.pumps.stbd)
    r.alerts.push({ kind:'err', text:'Crossover open with both Jabsco pumps running. Only one pump may run with crossover.' });
  if (state.crossover && totalOpenZones > 1 && eitherPumpOn)
    r.alerts.push({ kind:'err', text:'Crossover open with multiple zone valves open. Close all but one zone.' });
  if (state.crossover && fireOpen && eitherPumpOn)
    r.alerts.push({ kind:'err', text:'Crossover open with a 3-way in FIRE. Both 3-ways must be in BILGE.' });

  ['port','stbd'].forEach(side => {
    const p = r.pumps[side] = { active:false, mode:'off', reachable:[], wet:[], dry:[], state:'off' };
    if (!state.pumps[side]) return;
    p.active = true;
    if (state.threeWay[side] === 'fire') { p.mode = 'fire'; p.state = 'ok'; return; }
    p.mode = 'bilge';
    let reach = openByside[side].slice();
    if (state.crossover && !crossoverViolation) {
      reach = reach.concat(openByside[side === 'port' ? 'stbd' : 'port']);
    }
    p.reachable = reach;
    p.wet = reach.filter(z => state.bilgeWater[z]);
    p.dry = reach.filter(z => !state.bilgeWater[z]);

    if (crossoverViolation) p.state = 'err';
    else if (reach.length === 0) {
      p.state = 'err';
      r.alerts.push({ kind:'err', text:`${PUMPS[side].label} Jabsco dead-heading: no zone valve open. Impeller damage imminent.` });
    } else if (p.wet.length === 0) {
      p.state = 'err';
      r.alerts.push({ kind:'err', text:`${PUMPS[side].label} Jabsco running on air: all open compartments are dry. Impeller will burn out.` });
    } else if (p.dry.length > 0) {
      p.state = 'warn';
      r.alerts.push({ kind:'warn', text:`${PUMPS[side].label} Jabsco sucking air through ${p.dry.map(z=>ZONES[z].label).join(' + ')}. Close dry zone valves; only open valves to compartments with water.` });
    } else p.state = 'ok';
  });

  return r;
}

function renderFlows() {
  clearAllFlows();
  const r = solve();

  ['port','stbd'].forEach(side => {
    const p = r.pumps[side];
    if (!p.active) return;
    const cfg = PUMPS[side];

    if (p.mode === 'fire') {
      setPipeFlow(cfg.seacockPipe,   'ok');
      setPipeFlow(cfg.sidePipe3way,  'ok');
      setPipeFlow(cfg.sidePipePump,  'ok');
      return;
    }

    // Discharge pipe always reflects the active pump's state — water exits here.
    setPipeFlow(cfg.sidePipePump, p.state);

    // Dead-heading: no reachable zones at all. Light active pump's own suction
    // pipe in error to make the starved-pump cue obvious near the impeller.
    if (p.reachable.length === 0) {
      setPipeFlow(cfg.sidePipe3way, 'err');
      return;
    }

    // Individual zone-to-manifold pipes
    const wetState = (p.state === 'err') ? 'warn' : 'ok';
    const dryState = (p.state === 'err') ? 'err'  : 'warn';
    p.wet.forEach(zk => setPipeFlow(ZONES[zk].pipe, wetState));
    p.dry.forEach(zk => setPipeFlow(ZONES[zk].pipe, dryState));

    // Each side's 3-way-to-pump pipe lights up ONLY if that side actually has
    // reachable zones feeding the suction. In a crossover-only draw (active
    // pump's own side has no open valves) the active side's 3way-to-pump
    // stays dark — water enters via the opposite side's manifold.
    ['port','stbd'].forEach(s => {
      const sideHasZones = p.reachable.some(zk => ZONES[zk].side === s);
      if (!sideHasZones) return;
      const pipeId = PUMPS[s].sidePipe3way;
      setPipeFlow(pipeId, p.state);
    });

    // Crossover pipe lights up whenever water has to traverse it — i.e. any
    // reachable zone sits on the opposite side from the active pump.
    const usesCrossover = p.reachable.some(zk => ZONES[zk].side !== side);
    if (usesCrossover) {
      setPipeFlow('pipe-crossover', p.state);
    }
  });

  updateStatusUI(r);
  updateAlertsUI(r);
}

function updateStatusUI(r) {
  ['port','stbd'].forEach(side => {
    const el = document.getElementById('status-' + side);
    const p = r.pumps[side];
    let cls = 'state-off', txt = 'Off';
    if (p.active) {
      if (p.state === 'err')       { cls='state-err';  txt='FAULT'; }
      else if (p.state === 'warn') { cls='state-warn'; txt='Air-suck warning'; }
      else if (p.mode === 'fire')  { cls='state-fire'; txt='FIRE pumping'; }
      else { cls='state-ok'; txt = p.wet.length === 1 ? `Pumping ${ZONES[p.wet[0]].label.toLowerCase()}` : `Pumping ${p.wet.length} zones`; }
    }
    el.className = 'state ' + cls;
    el.querySelector('.text').textContent = txt;
  });
}

function updateAlertsUI(r) {
  const wrap = document.getElementById('alerts');
  wrap.innerHTML = '';
  r.alerts.forEach(a => {
    const div = document.createElement('div');
    div.className = 'alert ' + a.kind;
    div.appendChild(document.createTextNode(a.text));
    wrap.appendChild(div);
  });
}

function renderAll() {
  for (const k in ZONES) renderZoneValve(k);
  renderCrossover();
  renderThreeWay('port');
  renderThreeWay('stbd');
  renderPump('port');
  renderPump('stbd');
  renderBilgeHeads();
  renderFlows();
}

function wireClicks() {
  for (const k in ZONES) {
    const g = $(ZONES[k].id);
    if (g) g.addEventListener('click', () => { state.zones[k] = !state.zones[k]; renderAll(); });
    const bh = $(ZONES[k].bilge);
    if (bh) {
      bh.classList.add('bilge-clickable');
      bh.addEventListener('click', () => {
        if (state.mode === 'drill') return;
        state.bilgeWater[k] = !state.bilgeWater[k];
        renderAll();
      });
    }
  }
  const x = $(CROSSOVER.valveId);
  if (x) x.addEventListener('click', () => { state.crossover = !state.crossover; renderAll(); });
  ['port','stbd'].forEach(side => {
    const tw = $(PUMPS[side].threeway);
    if (tw) tw.addEventListener('click', () => {
      state.threeWay[side] = state.threeWay[side] === 'bilge' ? 'fire' : 'bilge';
      renderAll();
    });
    const pump = $(PUMPS[side].id);
    if (pump) pump.addEventListener('click', () => { state.pumps[side] = !state.pumps[side]; renderAll(); });
  });
}

