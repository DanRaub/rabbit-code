function pickRandomScenario() {
  let pick;
  do { pick = DRILL_SCENARIOS[Math.floor(Math.random() * DRILL_SCENARIOS.length)]; }
  while (state.drillScenario && pick.id === state.drillScenario.id && DRILL_SCENARIOS.length > 1);
  state.drillScenario = pick;
  document.getElementById('drill-brief').textContent = pick.brief;
  document.getElementById('drill-result').classList.remove('show','pass','fail');
  applyScenarioBilge(pick);
}

function setMode(m) {
  state.mode = m;
  document.getElementById('mode-explore').classList.toggle('active', m === 'explore');
  document.getElementById('mode-drill').classList.toggle('active', m === 'drill');
  document.getElementById('drill-panel').classList.toggle('show', m === 'drill');
  document.getElementById('btn-newdrill').style.display = m === 'drill' ? 'inline-flex' : 'none';
  applyDefaults();
  if (m === 'drill') pickRandomScenario();
  else state.drillScenario = null;
  renderAll();
}

function checkDrill() {
  if (!state.drillScenario) return;
  const r = state.drillScenario.check();
  const el = document.getElementById('drill-result');
  el.classList.remove('pass','fail');
  el.classList.add('show');
  if (r.ok) { el.classList.add('pass'); el.textContent = 'Correct'; }
  else      { el.classList.add('fail'); el.textContent = r.reasons[0] || 'Configuration incorrect'; }
}

